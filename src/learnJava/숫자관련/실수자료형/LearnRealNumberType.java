package learnJava.숫자관련.실수자료형;

public class LearnRealNumberType {

    public static void main(String[] args) {

        float aFloatNum = 1.12f;
        double aDoubleNum = 1.12;

        System.out.println(aFloatNum);
        System.out.println(aDoubleNum);

        aFloatNum += 0.12;
        aDoubleNum += 0.12;

        System.out.println(aFloatNum);
        System.out.println(aDoubleNum);
    }

}
